import os
import psutil
from prometheus_client import CollectorRegistry, Counter, Gauge, Histogram

METRICS_REGISTRY = CollectorRegistry()

# Gauges
process_memory_used_bytes = Gauge(
    "process_memory_used_bytes",
    "Current memory usage of the FastAPI process in bytes",
    registry=METRICS_REGISTRY,
)
system_memory_used_bytes = Gauge(
    "system_memory_used_bytes",
    "Current total system memory usage in bytes",
    registry=METRICS_REGISTRY,
)
llm_active_requests = Gauge(
    "llm_active_requests",
    "Number of LLM requests currently being processed",
    registry=METRICS_REGISTRY,
)

# Counters
llm_tokens_consumed_total = Counter(
    "llm_tokens_consumed_total",
    "Total number of prompt/input tokens consumed by the LLM",
    registry=METRICS_REGISTRY,
)
llm_tokens_generated_total = Counter(
    "llm_tokens_generated_total",
    "Total number of completion/output tokens generated by the LLM",
    registry=METRICS_REGISTRY,
)
memory_rejections_total = Counter(
    "memory_rejections_total",
    "Total number of requests rejected due to memory limits",
    registry=METRICS_REGISTRY,
)
http_requests_total = Counter(
    "http_requests_total",
    "Total number of HTTP requests received",
    ["endpoint", "status"],
    registry=METRICS_REGISTRY,
)
tts_characters_processed_total = Counter(
    "tts_characters_processed_total",
    "Total number of text characters processed by the TTS model",
    registry=METRICS_REGISTRY,
)
stt_audio_seconds_transcribed_total = Counter(
    "stt_audio_seconds_transcribed_total",
    "Total duration of audio in seconds processed by the STT model",
    registry=METRICS_REGISTRY,
)

# Histograms
llm_generation_latency_seconds = Histogram(
    "llm_generation_latency_seconds",
    "Latency of LLM text generation requests in seconds",
    registry=METRICS_REGISTRY,
)
audio_processing_latency_seconds = Histogram(
    "audio_processing_latency_seconds",
    "Latency of STT and TTS audio requests in seconds",
    registry=METRICS_REGISTRY,
)


def update_memory_metrics():
    # Update process memory
    process = psutil.Process(os.getpid())
    process_memory_used_bytes.set(process.memory_info().rss)

    # Update system memory
    system_memory_used_bytes.set(psutil.virtual_memory().used)
